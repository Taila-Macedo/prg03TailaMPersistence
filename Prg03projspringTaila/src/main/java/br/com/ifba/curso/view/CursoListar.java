    private void jTabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaMouseClicked
        int linha = jTabela.getSelectedRow();
        int coluna = jTabela.getSelectedColumn();
        if (linha < 0) return;

        String nomeCurso = jTabela.getValueAt(linha, 0).toString();
        List<Curso> cursosEncontrados = cursoService.findByNome(nomeCurso);

        if (cursosEncontrados.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum curso encontrado com o nome: " + nomeCurso);
            return;
        }

        Curso cursoSelecionado = cursosEncontrados.get(0);

        if (coluna == 4) { // Remover
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja remover o curso?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                cursoService.delete(cursoSelecionado);
                JOptionPane.showMessageDialog(this, "Curso removido com sucesso!");
                carregarTabela(cursoService.findAll());
            }
        }

        if (coluna == 5) { // Editar
            CursoEditar tela = new CursoEditar(cursoSelecionado, this.cursoService);
            tela.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_jTabelaMouseClicked

