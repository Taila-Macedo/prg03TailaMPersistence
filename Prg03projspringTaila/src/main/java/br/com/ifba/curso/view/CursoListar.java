    private void carregarTabela(java.util.List<Curso> cursos) {
     
    ImageIcon iconTrash = new ImageIcon(getClass().getResource("/images/trash.png"));
    ImageIcon iconEdit = new ImageIcon(getClass().getResource("/images/edit.png"));

    DefaultTableModel model = (DefaultTableModel) jTabela.getModel();
    model.setRowCount(0);// Limpa a tabela

    // Adiciona cada curso como uma linha da tabela
    
    if (cursos != null) {
        for (Curso c : cursos) {
            model.addRow(new Object[]{
                c.getNome(),
                c.getCodigoCurso(),
                c.getCargaHoraria(),
                c.isAtivo(),
                iconTrash,
                iconEdit
            });
        }
    }
    
    // Ajusta a largura das colunas
        jTabela.getColumnModel().getColumn(0).setPreferredWidth(150);  // Código
        jTabela.getColumnModel().getColumn(1).setPreferredWidth(100); // Nome
        jTabela.getColumnModel().getColumn(2).setPreferredWidth(100);  // Carga Horária
        jTabela.getColumnModel().getColumn(3).setPreferredWidth(50);  // Ativo
}
    

    
